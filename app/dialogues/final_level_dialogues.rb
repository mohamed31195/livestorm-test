def final_level_greetings
  puts 'Welcome to the final level.'
  puts 'I am Mahatmi Ghuru, the Master of Life.'
  puts 'If you solve my riddle I will give you the secret to infinite happiness in life.'
  puts '___________________________________________________________________________________'
end

def final_level_riddle
  puts 'Only one color, but not one size,'
  puts 'Stuck at the bottom, yet easily flies.'
  puts 'Present in sun, but not in rain,'
  puts 'Doing no harm, and feeling no pain.'
  puts 'What is it?'
end

def game_completed
  puts 'I am impressed. You are the first person to solve my riddle.'
  puts 'I will reward you with the secret to infinite happiness in life.'
  puts 'The secret to infinite happiness in life is...'
  puts "\n"
  puts 'To share it with others.'
  puts 'Thousands of candles can be lighted from a single candle,'
  puts 'and the life of the candle will not be shortened.'
  puts 'Happiness never decreases by being shared.'
  puts "\n"
  puts 'I hope you have learned the lesson my friend.'
  puts 'See you soon.'
  exit
end
